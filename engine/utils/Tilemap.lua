    -- @param start_vec - a vec2 of the starting point
    -- @param end_vec - a vec2 of the ending point
    function tilemap.Edge(start_vec, end_vec)
        local edge = {
            a = start_vec,
            b = end_vec,
            mid = (end_vec + start_vec) / 2
        }
        return edge
    end

    function tilemap.Cell(type)
        return {
            edges_exists = {
                n = false,
                e = false,
                s = false,
                w = false
            },
            edge_id = {
                n = nil,
                e = nil,
                w = nil,
                s = nil
            },
            type = type
        }
    end

    function tilemap.draw(self)
        for i = 1, #self.grid do
            local y = self.pos.y + (i - 1) * self.cell_size
            for j = 1, #self.grid[i] do
                if self.grid[i][j] == 1 then
                    local x = self.pos.x + (j - 1) * self.cell_size
                    love.graphics.rectangle('fill', x, y, self.cell_size, self.cell_size)
                end
            end
        end
    end

    return tilemap
end
